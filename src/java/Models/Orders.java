package Models;

/**
 *
 * @author ADMIN
 */
public class Orders {
     private int idOrder;
    private int idFood;
    private int quantity;
    private int price;

    public Orders() {
    }

    public Orders(int idOrder, int idFood, int quantity, int price) {
        this.idOrder = idOrder;
        this.idFood = idFood;
        this.quantity = quantity;
        this.price = price;
    }

    public int getIdOrder() {
        return idOrder;
    }

    public void setIdOrder(int idOrder) {
        this.idOrder = idOrder;
    }

    public int getIdFood() {
        return idFood;
    }

    public void setIdFood(int idFood) {
        this.idFood = idFood;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
}
